#Criar um programa que recebe 3 inteiros e imprime a soma e o produto entre eles

n1=int(input("Digite um valor:  "))
n2=int(input("Digite um valor:  "))
n3=int(input("Digite um valor:  "))

soma=n1+n2+n3
produto=n1*n2*n3

print(f"A soma entre eles é {soma} e o produto entre eles é {produto}")

